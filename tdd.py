def solution(x,y,operation):
	if operation =="+":
		return x+y
	elif operation =="-":
	    return x-y
	elif operation =="*": 
	    return x*y
	elif operation=="/":
	    return x/y  
	elif operation=="%":  
	    return x%y
	elif operation=="**":
		return x**y
	elif operation=="//":
		return x//y
	elif operation==">":
		return x>y
	elif operation=="!=":
	    return x!=y
	elif operation=="==":
		return x==y	     

    
